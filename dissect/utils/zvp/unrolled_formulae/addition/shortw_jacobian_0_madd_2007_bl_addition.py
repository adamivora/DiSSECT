from sage.all import *

pr = PolynomialRing(ZZ, ('a', 'b', 'X1', 'X2', 'Y1', 'Y2'), 6)
a, b, X1, X2, Y1, Y2 = pr.gens()
Z1, Z2 = 1, 1
formula = {}
Z1Z1 = Z1 ** 2
formula['Z1Z1'] = Z1Z1
U2 = X2 * Z1Z1
formula['U2'] = U2
t0 = Z1 * Z1Z1
formula['t0'] = t0
S2 = Y2 * t0
formula['S2'] = S2
H = U2 - X1
formula['H'] = H
HH = H ** 2
formula['HH'] = HH
I = 4 * HH
formula['I'] = I
J = H * I
formula['J'] = J
t1 = S2 - Y1
formula['t1'] = t1
r = 2 * t1
formula['r'] = r
V = X1 * I
formula['V'] = V
t2 = r ** 2
formula['t2'] = t2
t3 = 2 * V
formula['t3'] = t3
t4 = t2 - J
formula['t4'] = t4
X3 = t4 - t3
formula['X3'] = X3
t5 = V - X3
formula['t5'] = t5
t6 = Y1 * J
formula['t6'] = t6
t7 = 2 * t6
formula['t7'] = t7
t8 = r * t5
formula['t8'] = t8
Y3 = t8 - t7
formula['Y3'] = Y3
t9 = Z1 + H
formula['t9'] = t9
t10 = t9 ** 2
formula['t10'] = t10
t11 = t10 - Z1Z1
formula['t11'] = t11
Z3 = t11 - HH
formula['Z3'] = Z3
for key, value in formula.items():
    print(f'{key} = {value}')
